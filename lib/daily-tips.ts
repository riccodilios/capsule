function hashDayISO(dayISO: string): number {
  let h = 2166136261;
  for (let i = 0; i < dayISO.length; i++) {
    h ^= dayISO.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

export function getDailyTip(dayISO: string): Bilingual {
  const i = hashDayISO(dayISO) % DAILY_TIPS.length;
  return DAILY_TIPS[i]!;
}
